import java.util.Scanner;

/* a와 b 출력하기 */
public class Main {
    public static void main(String[] args) {

        Scanner scan = new Scanner(System.in);

        int a = scan.nextInt();
        int b = scan.nextInt();

        System.out.println("a = "  + a);
        System.out.println("b = "  + b);
    }
}